public class Integers {

  public static void main(String... args) {
    int bin = 0b1010;
    System.out.println(bin);
    int oct = 017;
    System.out.println(oct);
    int dec = 17;
    System.out.println(dec);
    int hex = 0x17ABcDeF;
    System.out.println(hex);
  }

}

