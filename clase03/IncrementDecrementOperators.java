public class IncrementDecrementOperators {

  final public static void main(String... args) {
    int a = 1;
    System.out.println(a);
    a = a + 1;
    System.out.println(a);
    a += 1;
    System.out.println(a);
    a++;
    System.out.println(a);
    ++a;
    System.out.println(a);
    // Tarea: Explicar la diferencia entre a++ y ++a
  }
}